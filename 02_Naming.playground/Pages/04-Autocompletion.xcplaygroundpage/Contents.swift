//: ## Typing Names and Autocompletion
//: On the next page, you’ll be typing in some names. When you do, the autocompletion menu will appear. Autocompletion helps you write code more quickly with fewer typing mistakes.
//:
//: Xcode keeps track of all the names you have defined. When you start typing, Xcode suggests names that it knows about. This is called _autocompletion_. The more you type, the more the list narrows down to what you are looking for.
//:
//: Use the Up Arrow and Down Arrow keys to move up and down the list.
//:
//: Hit the Return key to have Xcode fill in the selected name.
//: ![Autocompletion Popup Menu](autocomplete.png)
//: In the list, you will see names that you didn’t define. That is normal and it's perfectly fine to ignore those names.
//:
//: As part of your next experiment you’ll see autocompletion in action.
